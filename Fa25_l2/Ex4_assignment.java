public class Ex4_assignment {
  public static void main(String[] args) {
    int a = 10;
    System.out.println(a);
    a += 5; // a = a + 5
    System.out.println(a);
    a -= 10; // a = a - 10
    System.out.println(a);
    a *= 2; // a = a * 2
    System.out.println(a);
    a /= 3; // a = a / 3
    System.out.println(a);
    a %= 5; // a = a % 5
    System.out.println(a);
  }
}
